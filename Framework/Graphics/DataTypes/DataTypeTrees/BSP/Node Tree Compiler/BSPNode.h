#include "BSPPolygon.h"

//==================================================================================================================================
//==================================================================================================================================
namespace ZShadeSandboxBSP {
struct BSPNode
{
	BSPPolygon* splitterPolygon;
	BSPNode* 	frontChild;
	BSPNode*	backChild;
	
	// If true then no polygons are in this node.
	bool isLeaf;
	
	// False if this node is to the front of the parent (last poly in current poly list)
	// meaning that this node is an empty space or walkable space.
	// True if this node is to the back of the parent.
	bool isSolid;
	
	// In case the node does not have a valid front or back child
	// then it is properly initialized
	BSPNode()
	{
		splitterPolygon = 0;
		frontChild = 0;
		backChild = 0;
		isLeaf = isSolid = false;
	}
};
}
