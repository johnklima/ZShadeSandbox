#ifndef __INSERSIONSORT_H
#define __INSERSIONSORT_H
//===============================================================================================================================
//===============================================================================================================================
#include <vector>
using namespace std;
//===============================================================================================================================
//===============================================================================================================================
template <typename T>
class InsersionSort
{
public:

	InsersionSort() {}

	static void Sort(vector<T>& values)
	{
		T X;
		int j = 0;
		for (int i = 1; i < values.size(); i++)
		{
			X = values[i];

			for (j = i; j > 0 && values[j - 1] > X; j--)
			{
				values[j] = values[j - 1];
			}

			values[j] = X;
		}
	}
};
//===============================================================================================================================
//===============================================================================================================================
#endif//__INSERSIONSORT_H