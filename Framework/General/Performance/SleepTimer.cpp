#include "SleepTimer.h"
//================================================================================================================
//================================================================================================================
SleepTimer::SleepTimer() : counter(0), m_timeUp(false)
{
}
//================================================================================================================
SleepTimer::SleepTimer(const SleepTimer& other)
{
}
//================================================================================================================
SleepTimer::~SleepTimer()
{
}
//================================================================================================================
void SleepTimer::Sleep(unsigned long max_time)
{
	if (max_time == 0)
	{
		counter = 0;
		m_timeUp = true;
		return;
	}

	if (counter < max_time)
	{
		counter++;
		m_timeUp = false;
	}
	else
	{
		counter = 0;
		m_timeUp = true;
	}
}
//================================================================================================================
bool SleepTimer::IsTimeUp()
{
	return m_timeUp;
}
