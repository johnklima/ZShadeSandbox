#include "FPSCounter.h"
//===============================================================================================================================
//===============================================================================================================================
FPSCounter* FPSCounter::m_instance = 0;
//===============================================================================================================================
FPSCounter::FPSCounter()
:	m_FPS(0), m_count(0), m_startTime(timeGetTime())
{
}
//===============================================================================================================================
FPSCounter::~FPSCounter()
{
}
//===============================================================================================================================
FPSCounter* FPSCounter::GetInstance()
{
	if (m_instance == 0)
		m_instance = new FPSCounter();
	return m_instance;
}
//===============================================================================================================================
void FPSCounter::Update()
{
	m_count++;

	if (timeGetTime() >= (m_startTime + 1000))
	{
		m_FPS = m_count;
		m_count = 0;
		m_startTime = timeGetTime();
	}
}
//===============================================================================================================================
int FPSCounter::FPS()
{
	return m_FPS;
}
//===============================================================================================================================