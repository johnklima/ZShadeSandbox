#ifndef __ZEXCEPTION_H
#define __ZEXCEPTION_H
//======================================================================================================
//======================================================================================================
#include <string>
#include <exception>
using namespace std;
//======================================================================================================
//======================================================================================================
class ZException : public exception
{
	string msg;
public:
	
	ZException(const string m) : msg(m)
	{
	}
	
	virtual ~ZException() throw()
	{
	}
	
	virtual const char* what() const throw()
	{
		return msg.c_str();
	}
};
//======================================================================================================
//======================================================================================================
class ZEqualZeroException : public ZException
{
public:
	
	ZEqualZeroException()
	:  ZException("Value is equal to 0")
	{
	}
};
//======================================================================================================
//======================================================================================================
#endif//__ZEXCEPTION_H