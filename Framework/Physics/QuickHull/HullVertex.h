#ifndef __HULLVERTEX_H
#define __HULLVERTEX_H
//===============================================================================================================================
//===============================================================================================================================
#include "ZMath.h"
#include "HullFace.h"
//===============================================================================================================================
namespace ZShadeSandboxMath {
struct HullVertex
{
	// Point with this vertex
	ZShadeSandboxMath::XMMath3 point;

	// Back index into an array
	int index;

	// Current face that this vertex is outside of
	ZShadeSandboxMath::HullFace* face;

	HullVertex* next;
	HullVertex* prev;

	HullVertex() : face(nullptr)
	{
		point.x = point.y = point.z = index = 0;
	}
	
	HullVertex(float x, float y, float z, int idx) : face(nullptr)
	{
		point.x = x;
		point.y = y;
		point.z = z;
		
		index = idx;
	}
};
}
//===============================================================================================================================
//===============================================================================================================================
#endif//__HULLVERTEX_H