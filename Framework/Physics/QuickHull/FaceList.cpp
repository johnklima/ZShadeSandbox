#include "FaceList.h"
using ZShadeSandboxMath::FaceList;
//===============================================================================================================================
//===============================================================================================================================
FaceList::FaceList()
{
	clear();
}
//===============================================================================================================================
void FaceList::clear()
{
	head = tail = nullptr;
}
//===============================================================================================================================
void FaceList::add(ZShadeSandboxMath::HullFace* item)
{
	if (head == nullptr)
	{
		head = item;
	}
	else
	{
		tail->next = item;
	}

	item->next = nullptr;
	tail = item;
}
//===============================================================================================================================
ZShadeSandboxMath::HullFace* FaceList::first()
{
	return head;
}
//===============================================================================================================================
bool FaceList::isEmpty()
{
	return head == nullptr;
}
