#include "CirclePrimitive.h"
using ZShadeSandboxMath::CirclePrimitive;
//==============================================================================================================================
//==============================================================================================================================
CirclePrimitive::CirclePrimitive()
:   center(XMFLOAT3(0, 0, 0))
,   radius(0)
{
}
//==============================================================================================================================
CirclePrimitive::CirclePrimitive(XMFLOAT3 c, float r)
:   center(c)
,   radius(r)
{
}
//==============================================================================================================================
void CirclePrimitive::Set(XMFLOAT3 c, float r)
{
	center = c;
	radius = r;
}
//==============================================================================================================================
XMFLOAT3 CirclePrimitive::Center() { return center; }
//==============================================================================================================================
float CirclePrimitive::Radius() { return radius; }
//==============================================================================================================================