#include "BoundingBox.h"
using ZShadeSandboxMath::BoundingBox;
//==================================================================================================================================
//==================================================================================================================================
BoundingBox::BoundingBox()
{
	
}
//==================================================================================================================================
bool BoundingBox::Inside(XMFLOAT3 point)
{
	if (point.x >= x && point.x < x + width)
	{
		if (point.y >= y && point.y < y + height)
		{
			return true;
		}
	}
	
	return false;
}
//==================================================================================================================================
bool BoundingBox::Inside(float x, float y)
{
	if (x >= this->x && x < this->x + width)
	{
		if (y >= this->y && y < this->y + height)
		{
			return true;
		}
	}

	return false;
}
//==================================================================================================================================
bool BoundingBox::Intersect(BoundingBox box)
{
	bool bounds = !(x > box.x + box.width || x + width < box.x || y > box.y + box.height || y + height < box.y);
	
	return bounds;
}
