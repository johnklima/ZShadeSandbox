#ifndef __GAMESTATE_H
#define __GAMESTATE_H
//----------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------
enum GAME_STATE
{
	GS_NONE,
	GS_MAINMENU,
	GS_INGAME,
	GS_INGAMEMENU,
	GS_QUIT
};
//----------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------
class GameState
{
public:
	GameState();
	GameState(const GameState& l);
	~GameState();

	GAME_STATE GetState() { return m_state; }
	void SetState(GAME_STATE gs) { m_state = gs; }

	static void NewInstance() { gs_instance = new GameState(); }
	static GameState* GetInstance() { return gs_instance; }

private:
	static GameState* gs_instance;

	GAME_STATE m_state;
};
//----------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------
#endif//__GAMESTATE_H